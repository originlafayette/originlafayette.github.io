			<h2>Tippecanoe County History</h2>
	<p>Tippecanoe County is located in the Wabash Valley in west central Indiana. The Wabash River has played an important role in the county’s development attracting Indians, fur traders and missionaries to its shores. Before the white man came to that frontier, Indian tribes including the Wea, Potawatomi, Shawnee, Kickapoo and Winnebago inhabited the densely forested area. </p>
		<p>Seeking to establish a profitable fur trade with the Indians the French established Fort Ouiatenon in 1717. The fort was the first fortified European settlement in what would be Indiana. Several years later, French missionaries also came to the fort and soon it became a major outpost. </p>
		<p>During the Seven Years War, the French lost the fort to the British who, in turn, lost it to the Indians in 1763. In 1791 the fort was ordered destroyed. A replica of Fort Ouiatenon, decicated in 1907, commemorates the significance of the early settlement.</p> 
		<p>With the exception of Fort Ouiatenon, permanent settlement of the area did not begin until the early nineteenth century. By 1811 Prophet’s Town, located on the bluffs overlooking the river, was the largest military training camp for Indians in the Great Lakes’ region. Over 600 warriors from various tribes were assembled and trained in an effort to resist white settlement of the area. </p>
		<p>On November 7, 1811 this Indian confederacy was defeated at the Battle of Tippecanoe by William Henry Harrison and his army of 1000 men. This defeat marked the end of Indian domination in Indiana and the opening of the territory to white settlements.</p>
		<p>By 1816 the state of Indiana was admitted into the Union and ten years later, Tippecanoe County was organized. During the county’s formative years, most of the settlement activity occurred along the river. The county seat, Lafayette, was laid out along the eastern banks of the Wabash River in 1825.</p>
		<p>Both Lafayette’s and Tippecanoe County’s economic development was spurred by the passage of the Indiana Improvement Act of 1836. The Act provided for the construction of the Wabash and Erie Canal which reached Peru by 1837 and Logansport by 1838. By 1843, the canal was opened from Lafayette to Toledo.</p>
		<p>However, the canal’s “golden age” was short-lived. By 1852 the Monon Railroad was built through Lafayette and within the next two decades four other lines would follow marking the end of the canal era.</p> 
		<p>The railroad provided a link to outside markets for county’s farmers. Soon many of the townships had at least one “railroad town” which served as the commercial center and shipping point for local products. Towns such as Stockwell, Romney, Dayton and Montmorence experienced a boom period during the late nineteenth century. </p>
		<p>As Tippecanoe County developed economically, several significant educational institutions were established. The Farmer’s Institute was founded in 1851 as a subscription school by leading citizens of Union Township. As the enrollment increased the institutions grew to include a boarding house for the students. </p>
		<p>The schools were eventually turned over to the Society of Friends who continue to use the former school as a meetinghouse.</p>
		<p>Several years after the Farmers’ Institute was founded, the Methodists, who had platted a town adjacent to the tippecanoe battlefield, established the Battle Ground Collegiate Institute. The Institute operated from 1858 until 1882 when it closed due in part to competition from other schools.</p>
		<p>A member of the Institute’s Board of Trustees was instrumental in establishing the county’s leading educational facility, Purdue University. John Purdue donated $150,000 and 100 acres for the construction of the land grant college. Purdue University graduated its first student in 1875 and since that time has been one of the state’s leading institutions.</p>
		<p>During the twentieth century the tremendous growth and expansion of Lafayette has mirrored Tippecanoe County’s development. Diverse industries and the preeminence of Purdue University have made the county a leading economic force in west central Indiana. While Tippecanoe County can look forward to a bright future, the county can also look back upon a long and rich history which should be recognized and enjoyed. </p>
		<h2>Rating System</h2>
		<h3>Outstanding (O)</h3>
			<p>The “O” rating means that the property has enough historic or architectural significance that it is already listed O or should be considered for individual listing, in the National Register of Historic Places. “Outstanding” resources can be of local, state, or national importance.</p>
		<h3>Notable (N)</h3>
			<p>The rating of “N” means that the property did not quite merit an :Outstanding: rating, but still is above average in its importance. Further research of investigation may reveal that the property could be eligible for National Register listing.</p>
		<h3>Contributing (C)</h3>
			<p>A “C” rating was given to any properties meeting the basic inventory criterion of being pre-1940, but that are not important enough to stand on their own as individually “Outstanding” or “Notable.” Such resources are important to the density or continuity of the area’s historic fabric. “Contributing” properties can be listed in National Register of Historic Places if they are part of a historic district, but would not usually qualify individually.</p>
		<h3>Non-Contributing (NC)</h3>
			<p>Properties rated “NC” were not included in the inventory unless they were located within a historic district. Such properties are usually either post-1940 or they are older structures that have been badly altered and have lost historic character or they are otherwise incompatible with their historical surroundings. These properties are not eligible for the National Register.</p>
