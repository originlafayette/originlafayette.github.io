<p>
Another popular place within the City for local businessmen to build their homes was the Ninth Street Hill and State Street areas. Ninth Street was originally known as Clark Street, one of Lafayette’s few thoroughfares. For a time, Ninth street Hill was known as “Prospect Hill.” In July of 1860 Clark Street and Prospect Hill were renamed Ninth Street although Prospect Hill were renamed Ninth Street although Prospect Hill still appeared on abstracts as late as 1878. The years 1853 through 1872 represented a prosperous period of railroad expansion, and wealthier businessman began building homes on the hills surrounding Lafayette to the eat. The house (30003) of Judge Cyrus Ball, one of Tippecanoe County’s first judges, was built on “Prospect Hill: in the Italianate Villa style in 1865-1869. It remains today as one of Lafayette's most significant residences.  
<br>
<br>
Also located on the hill are several houses built by the Sample family. Robert Sample built two houses for his daughters upon their marriages: the Anna Sample Johnson and Ashley Johnson House (30021) and the Candance Sample Burt and Dr. Edgar V. Burt House (30004), both constructed in the Queen Anne style.  
Lafayette's leading citizens also built houses on State Street. William S. Potter, a local attorney and real estate investor, removed several buildings along State Street to enable him to move a building from the St. Louis Louisiana Purchase Exposition of 1912 to 920 State Street. Sections of three old Connecticut mansions, including the Hubbard-Slater Mansion and the Norwich House, had been used to construct Potter’s house (30056). Also located on the grounds of the Potter Mansion is the large brick carriage house (30057) used by the family.
<br>
<br>
With recent emphasis on preservation, many of the homes in the district have been returned to their original splendor after a period of decline during the 1950s and 1960s when many families moved to the suburbs. The area’s active neighborhood association has been responsible for much of the renewed interest in this architecturally significant area.
</p>

