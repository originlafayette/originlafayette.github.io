<p>
The Jefferson Historic District, named for the former Jefferson High School located at the edge of the district, was included within Lafayette’s early city limits and contains a large collection of railroad-era architecture. The John Metzger House (35037), built in 1869 in the Italianate Style, is representative of the district’s more elaborate houses. Other similar homes built between 1870 and 1885 include two house (35032, 35048) on North Street  and a duplex (35041), also located on North Street.
<br>
<br>
Because of the area’s proximity to the railroad and adjoining industries, the district was home to many of the town’s working-class families. Small duplexes such as two Italianate cottages (35024, 35025) on Brown Street provided inexpensive housing for workers in local businesses. One of the few non residential buildings in the district is the former German Methodist Episcopal Church (35014). Built in 1885, this Gothic Revival building contains impressive stained-glass windows. The congregation conducted services in German until the early twentieth century.
</p>

