<p>
The Park Mary Historic District consists almost entirely of Orth’s Addition platted by Godlove S. Orth and his wife, Mary Ann Orth. The district is named for the park which was located within the plat, Park Mary, named for Mrs. Orth. Park Mary was bordered by Eleventh, Amanda (now Twelfth), Salem and Tippecanoe Streets. Mr. Orth was a prominent Lafayette attorney who held several state offices, was elected to the United State Congress in 1862, and served a United States Ambassador. The Orth family home, now gone, was located at the northeast corner of Ninth and Union Streets. 
<br>
<br>
Many of the homes within the district date from the mid-nineteenth century and are if Italianate design. Several of the noteworthy houses include Quincy Adams Perrin in 1868. The house’s second owner, Samuel Seawright, added the distinctive Levering House (36021), built c.1850, is located on William, operated a real estate and insurance business in Lafayette. The Harvey J. Sawyer House (35152), located across from Park Mary, was built in 184. Mr. Sawyer was a cabinetmaker. 
<br>
<br>
Two of the district's noteworthy public buildings dating from the early twentieth century are the Christian Reformed Church (36022) built in 1929 in the Gothic Revival style, and the Neoclassical-style Lafayette Armory (36098), built two years earlier and still use. 
</p>

