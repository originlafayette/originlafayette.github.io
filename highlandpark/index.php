<p>
The Highland Park Historic District encompases the area lying within the plat of Highland Park as well as additional property located north of the platted area. Laid out in the early 1850s , Kossuth Street was for more than 40 years the southern boundary of Lafayette until the platting of Highland Park in 1891. The new addition was formerly a section of the Ross and Reynolds farm pastures where in 1853, the second Indiana State Fair was held drawing over 60,000 people.
<br>
<br>
The Highland Park Land Company was formed by James M. Reynolds, William Ross and David Linn Ross. Initially the area contained 136 lots. A southern addition, Highland Park II, was platted in 1893 and added 28 more lots. Included in the plat was a lot reserved for a public school which became the site of the Columbian School. Today, the Highland School (33109), constructed in 1924 is located on the site. 
<br>
<br>
Highland Park developed as a typical, early twentieth century neighborhood. Bungalows and period revival style houses lined the winding tree-lined streets. The residents ranged from merchants and businessmen to two Indiana governors.
</p>

