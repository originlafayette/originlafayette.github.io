	<p>The Centennial Historic District lies just north of Lafayette’s commercial district. The area was partially included within Lafayette's original plat and contains examples of most of the architectural styles found in the town. As one of Lafayette's oldest neighborhoods, it contains many significant religious and educational institutions as well as homes associated with early prominent citizens.</p>
		<p>The area was first developed during the 1830s as Lafayette's boundaries expanded north and east from the Wabash River. The Reverend Samuel Johnson House (27079), built in 1844, is Lafayette’s oldest remaining structure. The Richard Dehart House (27131) was constructed one year later. In 1843 a period of growth began as the Wabash and Erie Canal was opened along the district’s western edge. The resulting commercial growth along its path included mills, warehouses and wharves. Several residences have survived from the canal era which only lasted until the early 1850s. The T.P. Emerson House (27033), the Jones-Carnahan House (27052), and houses (27087, 27115) at 636 Ferry Street and 731-715 North Fourth Street reflect this boom period. </p>
		<p>The coming of the town’s first railroad in 1853 quickly put and end to the Wabash and Erie Canal. During the next three decades as Lafayette experienced tremendous physical and economic growth, the Centennial District took on much of its present appearance.The industrial development along the area’s western edge which accompanied the railroad necessitated the construction of workers’ housing. Small, frame structures and brick rowhouses (27148) provided affordable housing.The neighborhood’s more affluent residents built homes in the popular styles of the period. Ferry Street is lined with Italianate townhouses and single-family homes such as the Handley House (27080) and the Oppenheimer House (27170). </p>
		<p>The district contains the highest concentration of significant religious and educational architecture in the city. St. John’s Episcopal Church (27179), designed by English architect William Tinsley between 1851-1858, is the district’s oldest surviving church. Trinity Methodist Episcopal Church (27160), St. James Lutheran Church (27014) and St. Boniface Catholic Church (27249) are all outstanding examples of the Gothic Revival style.The Bethel African Methodist Episcopal Church (27093) was the site of Lafayette’s first black school. The school opened as a private school in 1867. The building is now used as a  Baptist church.The Albert A. Wells Memorial Public Library (27058), designed by noted local architect Walter Scholer, is one of the foremost examples of the Neoclassical style in Tippecanoe County. The neighborhood’s historical and architectural significance was recognized when the Centennial Historic District was listed in the National Register of Historic Places on June 16, 1983.</p>

