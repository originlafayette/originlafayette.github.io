		<h2>Outstanding (O)</h2>
			<p>The “O” rating means that the property has enough historic or architectural significance that it is already listed O or should be considered for individual listing, in the National Register of Historic Places. “Outstanding” resources can be of local, state, or national importance.</p>
		<h2>Notable (N)</h2>
			<p>The rating of “N” means that the property did not quite merit an :Outstanding: rating, but still is above average in its importance. Further research of investigation may reveal that the property could be eligible for National Register listing.</p>
		<h2>Contributing (C)</h2>
			<p>A “C” rating was given to any properties meeting the basic inventory criterion of being pre-1940, but that are not important enough to stand on their own as individually “Outstanding” or “Notable.” Such resources are important to the density or continuity of the area’s historic fabric. “Contributing” properties can be listed in National Register of Historic Places if they are part of a historic district, but would not usually qualify individually.</p>
		<h2>Non-Contributing (NC)</h2>
			<p>Properties rated “NC” were not included in the inventory unless they were located within a historic district. Such properties are usually either post-1940 or they are older structures that have been badly altered and have lost historic character or they are otherwise incompatible with their historical surroundings. These properties are not eligible for the National Register.</p>
