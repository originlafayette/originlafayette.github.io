	<p>The Ellsworth Historic District is located southeast of Lafayette’s business district. Most of the district’s buildings were constructed during the mid to late nineteenth century although some earlier residences have remained. The area is named for Henry Leavitt Ellsworth, son of Supreme Court Justice Oliver Ellsworth. Ellsworth was appointed United States Commissioner of Patents in 1835. He wrote a book entitled, Valley of the Upper Wabash, Indiana with Hints on Its Agricultural Advantages, which promoted investment in Indiana and Illinois farmland. </p>
		<p>Ellsworth owned large amounts of land in Lafayette including the area within the historic district. He laid out several subdivisions between 1844 and 1846, and soon merchants, bankers and craftsmen settled in the area. Most of the earliest structures are located in the district's northern section. The Moses Fowler House (28033), built in 1851-1852, is the county’s premier residential example of the Gothic Revival style. Both the Fowler House and the William Potter House (28012), built in 1855, are listed in the National Register of Historic Places. The railroad line which runs along the district's western edge was the first constructed in Lafayette. The New Albany and Salem Railroad reached the town in 1853. The Toledo, Wabash and Western line was completed along the district’s southeast border in 1856. </p>
		<p>As in other sections of Lafayette, the ensuing prosperity produced a building bloom, and many large, elaborate homes were built in the area. James Alexander who designed the Tippecanoe County Courthouse, built his house (28138) in about 1880. James Falley had his Italian Villa house (28086) built between 1862-1864. Lewis Falley, Jr. constructed one of Lafayette;s few remaining examples of the Second Empire style (28014) on South Street. Adjacent to Falley’s house is an outstanding example of a rowhouse with the Italianate features. The Howe Block (28015) was constructed about 1870.</p>
		<p>Several noteworthy religious buildings remain in the district. Temple Israel (28188), which was placed in the National Register of Historic Places in 1982, is the oldest known structure in Indiana originally built for a Jewish congregation. Formed in 1849, the Ahavas Achim congregation was the second organized in Indiana. The Italianate synagogue was constructed in 1866-1867.The Second Presbyterian Church (28002) was built on land donated by Moses Fowler;s wife in 1893. The church is the best example of the Richardsonian Romanesque style in Tippecanoe County. The  Ellsworth Historic District was placed in the National Register of Historic Places on December 30, 1986.</p>

