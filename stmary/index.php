	<p>The St. Mary’s Historic District lies just southeast of the Centennial Historic District. As Lafayette expanded to the north, the St. Mary’s area became a popular place to live for the city’s wealthier citizens, especially along Columbia Street district’s primary thoroughfare.</p>
		<p>In 1864 St. Mary’s Catholic Church relocated from Fifth and Brown Streets to Columbia Street. The church was constructed on the site of the former County Seminary. When the church moved so did many members of the congregation so the St. Mary’s became both a religious and social center for the neighborhood.</p>
		<p>Many of the the homes date from the 1860s and 1870s and include fine examples of the Italianate, Greek Revival and Queen Anne styles as well as vernacular house types. Most of the people who built their homes in this area were Lafayette businessmen. The Greek Revival house (31005) at 1202 Columbia Street was built in 1862 by James Ball , a local wholesale grocer, whose name remains stamped into the front steps. Across the street from the Ball house is the James H. Ward House (31004). Mr. Ward was actively involved with his brother, William, in a local carpet and wallpaper business.</p>
		<p>The James Murdock House (31026) also located on Columbia Street was constructed c.1890. Mr. Murdock moved into the house in approximately 1891 after leaving his post as warden of the prison at Michigan City. He was also active in Lafayette as the operator of a grocery and produce business and in bridge and road construction. The home was later sold to Ferdinand Dryfus who, together with his brother Leopold, managed the Dryfus Packing and Provision Company.</p>

