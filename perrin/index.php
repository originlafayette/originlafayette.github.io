<p>
The Perrin Historic District developed on a terraced area of the Wabash Valley with curving streets that responded to the topography. The land in the district was first considered for development by Lawrence B. Stockton’s Woods. However, it was not developed until 1873 when James J. Perrin offered 63 acres for sale.Perrin had moved to Indiana from Virginia in 1847. He maintained a farm in Carroll County, Indiana, until 1869 when he moved to Lafayette where he became president of the Indiana National Bank, renamed the Perrin National Bank. Perrin, who along with several others platted the land as Perrin;s Addition in 1873, became the sole developer within three years of its purchase. The former Lake Erie and Wabash Railroad and the terraced lands of the river valley form the western edge of the district and provided the boundaries for early development in the bottom lands. The Perrin Addition was one of the first developments to break this tradition by building directly on the terraced area. 
<br>
<br>

Due to the area’s topography, the traditional grid system found in other parts of Lafayette was abandoned in favor of a winding, curved street pattern. The homes in the area were built conforming to this pattern with a variety of setbacks and sitings making the area visually unique. Most of the district’s significant architecture was constructed during the late nineteenth and early twentieth centuries. One of the area’s focal points is the James Perrin House (29077) purchased from James Blake in 1869. Originally constructed in the Italianate Style, the house was later redesigned in the Queen Anne Style. Nearby stand two houses Perrin had built for his sons. The William Perrin House (29065) has unfortunately suffered major altercations and the John Perrin House (29066) was built in 1890 in the Queen Anne style.  
<br>
<br>
Just across from the Perrin house are three of the district’s most significant structures. The Weigle House (29058), the Foit-Perrin House (29059) and the William Crouse House (29060) were all built between 1888-1893. These houses, combined with the Perrin houses, make this block one of the district’s most significant. Although these large, elaborate houses tend to dominate the area, the neighborhood was also home to many of Lafayette’s middle and working classes. Modest bungalows (29142-29145) and small frame cottages (29181-29184) are found throughout the district. The Perrin Historic District was listed in the National Register of Historic Places in September of 1979.</p>

